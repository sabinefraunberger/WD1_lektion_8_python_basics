# mood-checker
user_mood = input("What mood are you in today?: ")

if user_mood == "happy":
    print("It is great to see you happy!")
elif user_mood == "nervous":
    print("Take a deep breath 3 times.")
elif user_mood == "sad":
    print("Don't worry. Tomorrow it will be better!")
elif user_mood == "excited":
    print("Wow, that's awesome!")
elif user_mood == "relaxed":
    print("Yeah, it's nice to see you relaxed! ")
else:
    print("I don't recognize this mood")
